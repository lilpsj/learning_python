'''
기본 입출력 - 이스케이스 문자
\n : 줄바꿈, 개행
\t : 탭 : 탭이라는 단위만큼 띄우기
\a : 벨 :알람(소리)을/를 울려라
'''

print("1. Hello World~!")

print("2. Hello\nWorld~!")

print("3. Hello\tWorld~!")

print("4. Hello\aWorld~!")

# ','를 이용해 문자열과 문자열/정수/실수(데이터)를 연결
print(" I am", " a Python", " Programmer")

print("사람의 체온은 ", 36.5)

print("이 교재의 마지막 페이지는 ", 390, "쪽입니다.")

print("철수의 국어 점수 = ", 55)
print("철수의 수학 점수 = ", 79)
